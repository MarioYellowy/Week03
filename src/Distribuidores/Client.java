package Distribuidores;

public class Client {
    private String name, contact;

    public Client(String name, String contact) {
        this.name = name;
        this.contact = contact;
    }

    public String getName() {
        return name;
    }

    public void setName() {
        this.name = name;
    }

    public String getContact() {
        return contact;
    }

    public void setContact() {
        this.contact = contact;
    }
}
